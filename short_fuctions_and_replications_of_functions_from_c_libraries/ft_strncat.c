char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;

	i = -1;
	while (dest[++i] != '\0')
	{
	}
	while (*src != '\0' && nb > 0)
	{
		dest[i] = *src;
		src++;
		i++;
		nb--;
	}
	dest[i] = '\0';
	return (dest);
}
